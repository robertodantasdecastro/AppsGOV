	<td bgcolor="#FFFFFF" valign="top" align="left">
	<p>DECRETO N.º10.087, DE 11  OUTUBRO DE 2013.
	REGULAMENTA EM ÂMBITO MUNICIPAL A LEI FEDERAL Nº 12.527 DE 18 DE NOVEMBRO DE
	2011, QUE DISPÕE SOBRE  O ACESSO À INFORMAÇÃO.
	O PREFEITO DO MUNICÍPIO DE NATAL, no uso das atribuições que lhe são conferidas pela Lei
	Orgânica do Município de Natal, artigo  55,  VI, DECRETA:</p>
	<p>&nbsp;</p>
	Art. 1º. Ficam estabelecidos os procedimentos e as normas a serem adotados para garantir o acesso às informações da administração pública municipal, previsto no inciso XXXIII do caput do art. 5º, no inciso II, do § 3º, do art. 37 e no § 2º, do art. 216, da Constituição Federal, em conformidade com disposições da Lei Federal n. 12.527, de 18 de novembro de 2011.<br>
	<p>&nbsp;</p>
	Art. 2º. Os órgãos da administração direta e indireta do Poder Executivo assegurarão às pessoas naturais e jurídicas o direito de acesso à informação, que será efetivado mediante procedimentos objetivos e ágeis, de forma transparente, clara e em linguagem de fácil compreensão, observados os princípios da administração pública e as disposições deste decreto.<br>
	Parágrafo único.  Ficam subordinadas ao regime deste decreto as entidades privadas, relativamente aos recursos que receberem do Poder Executivo Municipal, mediante subvenções, contrato administrativo, termo de parceria, convênios, acordo, ajustes ou outros instrumentos congêneres.<br>
	<p>&nbsp;</p>
	<p>Art. 3º. O acesso à informação disciplinado neste decreto não se aplica:<br>
	I - às informações relativas à atividade empresarial de pessoas  físicas ou jurídicas de direito privado, obtidas  por outros  órgãos  ou entidades  no exercício  de atividade  de controle, regulação e supervisão da atividade econômica cuja divulgação possa representar  vantagem competitiva a outros agentes econômicos;<br>
	II - às hipóteses  de sigilo previstas na legislação, como fiscal, bancária, comercial, profissional, industrial e segredo de justiça;<br>
	III  – às informações relacionadas  à pessoa  natural identificada ou identificável, relativa  à intimidade, vida privada, honra e imagem.</p>
	<p>&nbsp;</p>
	<p>Art. 4º. Fica criado  o Serviço de Informação ao Cidadão - SIC,  coordenado pela Secretaria Municipal  de Planejamento, Fazenda  e Tecnologia  da Informação,  acessível  via  web,  no endereço http://natal.rn.gov.br/leideacesso  ou através do Protocolo Geral que ficará instalado na Rua Dr. Ewerton Dantas Cortez, 1432,  Tirol, CEP 59020,  Natal/RN.
	Parágrafo  único. Cabe ao Serviço de Informação ao Cidadão - SIC:<br>
	I - disponibilizar informações em conformidade com a Lei nº 12.527, de 28 de novembro de
	2011, por meio eletrônico;<br>
	II - disponibilizar atendimento  presencial ao público;<br>
	III - receber, autuar e processar, para respostas, os pedidos de acesso às informações;<br>
	IV - orientar o interessado, quanto ao seu pedido, o trâmite, o prazo da resposta e sobre as informações disponíveis no site eletrônico http://natal.rn.gov.br/leideacesso/;<br>
	V - zelar pelo atendimento dos prazos assinalados para apresentação de respostas; VI - elaborar relatório mensal dos atendimentos.</p>
	<p>&nbsp;</p>
	<p>Art. 5º. Qualquer interessado, devidamente identificado, poderá ter acesso às informações referentes aos órgãos e às entidades  municipais, preferencialmente,  no site http://natal.rn.gov.br/leideacesso/ e, na impossibilidade de utilização desse  meio, apresentar  o pedido no Serviço de Informação ao Cidadão - SIC, conforme  Anexo I.<br>
	§ 1º. O pedido de acesso  à informação deverá conter:<br>
	I - nome do requerente;<br>
	II - número de documento de identificação válido;<br>
	III - especificação, de forma clara e precisa, da informação requerida; e<br>
	IV  - endereço  físico ou eletrônico do requerente, para recebimento de comunicações ou da resposta requerida.<br>
	§ 2º. Não serão atendidos pedidos de acesso à informação:<br> 
	I - genéricos;<br>
	II - desproporcionais ou desarrazoados; ou<br>
	III  - que exijam trabalhos  adicionais de análise, interpretação ou consolidação de dados e informações, ou serviço de produção ou tratamento de dados, que não sejam de competência do órgão ou entidade municipal.<br>
	§ 3º. Na hipótese do inciso III do § 2º, o órgão ou entidade deverá, caso tenha conhecimento, indicar o local onde  se encontram as informações a partir das quais o requerente poderá realizar a interpretação, consolidação ou tratamento de dados.</p>
	<p>&nbsp;</p>
	<p>Art. 6º. As informações solicitadas serão  prestadas  pelo Serviço de Informação ao Cidadão - SIC, no prazo de, até, vinte dias.<br>
	<p>&nbsp;</p>
	§ 1º. O prazo referido no caput poderá ser prorrogado, por mais dez dias, mediante justificativa expressa do responsável pela prestação  da informação, da qual será dada ciência ao requerente.<br>
	§ 2º. Não sendo possível o fornecimento da informação, o Serviço de Informação ao Cidadão - SIC deverá: I - apresentar ao requerente as razões de fato ou de direito da recusa, total ou parcial, do acesso pretendido; ou<br>
	II - comunicar que não possui a informação, indicando, se for do seu conhecimento, o órgão, a entidade ou a organização, não pertencente à Administração  Pública Municipal, que deve detê-la.<br>
	§ 3º. Quando não for autorizado o acesso, por se tratar de informação reservada ou sigilosa, o requerente  será informado sobre a possibilidade  de recurso,  conforme anexo II.<br>
	§ 4º. Caso a informação solicitada esteja disponível ao público em formato impresso, eletrônico 
	ou em qualquer outro meio de acesso universal, será informado ao requerente o lugar e a forma pela qual se poderá consultar e obter a referida informação, desonerando a Administração 
	Municipal da obrigação de seu fornecimento direto, salvo se o requerente declarar não dispor 
	de meios para realizar, por si mesmo, tais procedimentos.</p>
	<p>&nbsp;</p>
	<p>Art. 7º. A busca e o fornecimento da informação são gratuitos, ressalvada a cobrança do 
	valor referente ao custo dos serviços e dos materiais utilizados, tais como reprodução de 
	documentos, mídias digitais e postagem, cujos valores serão fixados em ato a ser emanado 
	pela Secretaria Municipal de Tributação.<br>
	<p>&nbsp;</p>
	§ 1º. Fica isento de ressarcir os custos dos serviços e dos materiais utilizados aquele cuja 
	situação econômica não lhe permita fazê-lo sem prejuízo do sustento próprio ou da família.<br>
	§ 2º. Poderá ser beneficiado com a isenção de pagamento aquele que estiver inscrito no 
	Cadastro Único; e for membro de família de baixa renda (com renda mensal per capita de até 
	meio salário mínimo ou a que possua renda familiar mensal de até três salários mínimos), 
	devendo informar o Número de Identificação Social (NIS).<br>
	§ 3º. Caso seja requerida justificadamente a concessão da cópia de documento, com 
	autenticação, poderá ser designado um servidor para certificar que confere com o original.</p>
	<p>&nbsp;</p>
	<p>Art. 8º. As informações de interesse público serão disponibilizadas no sítio eletrônico http://natal.rn.gov.br/leideacesso/, as quais serão atualizadas, rotineiramente, e deverá atender, 
	entre outros, aos seguintes requisitos:<br>
	<p>&nbsp;</p>
	I - conter formulário para requerimento de acesso a informação;<br>
	II - conter ferramenta de pesquisa de conteúdo que permita o acesso a informação, de forma 
	objetiva, transparente, clara e em linguagem de fácil compreensão;<br>
	III - possibilitar a impressão de relatórios, planilhas e texto, de modo a facilitar a análise das informações;<br>
	IV - garantir a autenticidade e a integridade das informações disponíveis para acesso;<br>
	V - manter atualizadas as informações disponíveis para acesso;<br>
	VI - indicar local que permita ao interessado comunicar-se pessoalmente com o Serviço de 
	Informação ao Cidadão - SIC; e<br>
	VII - adotar as medidas necessárias para garantir a acessibilidade de conteúdo para pessoas 
	com deficiência, nos termos da legislação própria.
	Parágrafo único. É dever dos órgãos e entidades municipais promover, independente de 
	requerimento, a divulgação em seus sítios na Internet de informações de interesse coletivo ou 
	geral por eles produzidas.</p>
	<p>&nbsp;</p>
	<p>Art. 9º. Deverão ser disponibilizadas no endereço eletrônico http://natal.rn.gov.br/leideacesso/ 
	as seguintes informações de interesse público:<br>
	<p>&nbsp;</p>
	I - estrutura organizacional, competências, legislação aplicável, principais cargos e seus 
	ocupantes, endereço e telefones das unidades, horários de atendimento ao público;<br>
	II - programas, projetos, ações, obras e atividades, com indicação da unidade responsável, 
	principais metas e resultados e, quando existentes, indicadores de resultado e impacto;<br>
	III - receita orçamentária arrecadada; <br>
	IV - repasses ou transferências de recursos financeiros;<br>
	V - execução orçamentária e financeira detalhada em nível de grupo de despesa;<br>
	VI - licitações realizadas e em andamento, com editais, anexos e resultados, além dos contratos 
	firmados e notas de empenho emitidas;<br>
	VII - remuneração e subsídio dos cargos, postos, graduação, função e emprego público;<br>
	VIII - respostas a perguntas mais frequentes da sociedade; e<br>
	IX - contato da autoridade de monitoramento, designada nos termos do art. 40, da Lei n. 12.527/2011, 
	e telefone e correio eletrônico do Serviço de Informações ao Cidadão -SIC.
	Parágrafo único. As informações poderão ser disponibilizadas por meio de ferramenta de redirecionamento 
	de página na Internet, quando estiverem disponíveis em outros sítios governamentais.</p>
	<p>&nbsp;</p>
	<p>Art. 10. No caso de indeferimento de acesso às informações ou às razões da negativa do 
	acesso, poderá o interessado interpor recurso contra a decisão, no prazo de dez dias, a contar 
	da sua ciência, conforme Anexo II.<br>
	<p>&nbsp;</p>
	§ 1º. O recurso será apresentado no Serviço de Informação ao Cidadão - SIC, que o encaminhará 
	à autoridade que exarou a decisão impugnada, devendo se manifestar no prazo de dez dias.<br>
	§ 2º. Mantida novamente a negativa, o recurso será encaminhado à Comissão Mista de 
	Reavaliação de Informações.</p>
	<p>&nbsp;</p>
	<p>Art. 11. Fica criada a Comissão Mista de Reavaliação de Informações com a seguinte 
	representação:<br>
	<p>&nbsp;</p>
	I - um representante da Secretaria Municipal de Administração e Gestão Estratégica;<br>
	II - um representante da Secretaria Municipal de Planejamento, Fazenda e Tecnologia da Informação;<br>
	III - um representante da Ouvidoria Geral do Município;<br>
	IV - um representante da Controladoria Geral do Município;<br>
	V - um representante da Procuradoria-Geral do Município.<br>
	<p>&nbsp;</p>
	§ 1º. A indicação e nomeação dos membros da Comissão Mista de Reavaliação de Informações 
	é da responsabilidade do Prefeito Municipal.<br>
	§ 2º. O membro da Comissão Mista de Reavaliação de Informações poderá ser desligado da função nos casos 
	de renúncia, falta injustificada a três reuniões consecutivas ou desligamento do órgão que representa.<br>
	§ 3º. A Presidência da Comissão Mista de Reavaliação de Informações será exercida pelo 
	representante da Secretaria Municipal de Planejamento, Fazenda e Tecnologia da Informação.<br>
	§ 4º. A participação dos integrantes da Comissão de Reavaliação de Informações é considerada 
	como serviço público relevante.</p>
	<p>&nbsp;</p>
	<p>Art. 12. Cabe à Comissão Mista de Reavaliação de Informações:
	I - manter registro dos titulares de cada órgão e entidade do Poder Executivo Municipal, para 
	decisão quanto ao acesso a informações e dados sigilosos ou reservados da respectiva área;<br>
	II - requisitar da autoridade que classificar informação como sigilosa, esclarecimentos ou 
	acesso ao conteúdo, parcial ou integral da informação;<br>
	III - rever a classificação de informações sigilosas, de ofício ou mediante provocação de pessoa interessada observado o disposto na legislação federal sobre essa classificação;<br>
	IV   - recomendar  medidas  para aperfeiçoar  as normas  e procedimentos  necessários  à implementação deste decreto;<br>
	V  - manifestar-se sobre reclamação apresentada  contra omissão ou recusa de autoridade municipal, quanto ao acesso  à informações.</p>
	<p>&nbsp;</p>
	<p>Art. 13.  Ao Presidente  da Comissão Mista de Reavaliação de Informações cabe: <br>
	I - presidir os trabalhos da Comissão;<br>
	II - aprovar a pauta das reuniões ordinárias e as ordens do dia das respectivas sessões;<br>
	III - dirigir, intermediar as discussões, de forma que todos participem e coordenar os debates, interferindo para esclarecimentos;<br>
	IV - designar o membro secretário, para lavratura das atas de reunião;<br>
	V - convocar reuniões extraordinárias e as respectivas sessões; e<br>
	VI - remeter  ao Secretário  Municipal de Planejamento, Fazenda e Tecnologia da Informação a ata com as decisões tomadas pelo colegiado, para serem encaminhadas ao Prefeito Municipal.
	<p>&nbsp;</p>
	§ 1º. A Comissão Mista de Reavaliação de Informações  reunir-se-á,  sempre  que convocada  pelo presidente.<br>
	§ 2º.  A Comissão Mista de Reavaliação de Informações atuará  junto à Secretaria  Municipal de
	Planejamento, Fazenda e Tecnologia da Informação.</p>
	<p>&nbsp;</p>
	<p>Art.  14. Não poderá  ser negado acesso  às informações necessárias  à tutela  judicial ou administrativa de direitos fundamentais.
	Parágrafo único. O requerente deverá apresentar razões que demonstrem a existência de nexo entre as informações requeridas e o direito que se pretende proteger.</p>
	<p>&nbsp;</p>
	<p>Art. 15. Constituem condutas  ilícitas que ensejam responsabilidade do agente público:<br>
	<p>&nbsp;</p>
	I - recusar-se  a fornecer informação requerida nos termos deste decreto, retardar deliberadamente o seu fornecimento ou fornecê-la intencionalmente de forma incorreta, incompleta ou imprecisa;<br>
	II - utilizar indevidamente,  bem como subtrair, destruir, inutilizar, desfigurar, alterar ou ocultar, total ou parcialmente, informação que se encontre sob sua guarda ou a que tenha acesso ou conhecimento em razão do exercício das atribuições de cargo, emprego ou função pública;<br>
	III - agir com dolo ou má-fé na análise das solicitações de acesso à informação;<br>
	IV  - divulgar ou permitir a divulgação ou acessar ou permitir acesso  indevido à informação sigilosa ou informação pessoal;<br>
	V - impor sigilo à informação para obter proveito pessoal ou de terceiro, ou para fins de ocul- tação de ato ilegal cometido por si ou por outrem;<br>
	VI - ocultar da revisão de autoridade superior competente informação sigilosa para beneficiar a si ou a outrem, ou em prejuízo de terceiros; e<br>
	VII - destruir ou subtrair, por qualquer meio, documentos concernentes  a possíveis violações de direitos humanos por parte de agentes do Estado.<br>
	<p>&nbsp;</p>
	§ 1º . Atendido o princípio do contraditório, da ampla defesa e do devido processo  legal, as condutas descritas no caput serão consideradas, para fins do disposto na  Lei n. 1.517  de
	23 de dezembro de 1965 - Estatuto do Funcionário Público Municipal , e suas alterações, infrações administrativas, que deverão ser apenadas  segundo os critérios nela estabelecidos.<br>
	§ 2º . Pelas condutas descritas no caput, poderá o agente público responder,  também, por improbidade administrativa, conforme o disposto nas Leis nos 1.079,  de 10 de abril de 1950, e 8.429, de 2 de junho de 1992.</p>
	<p>Art. 16. A pessoa  física ou entidade  privada que detiver informações  em virtude de vínculo de qualquer natureza com o poder público e deixar de observar o disposto neste decreto estará sujeita às seguintes sanções:<br>
	I - advertência; <br>
	II - multa;<br>
	III - rescisão  do vínculo com o poder público;<br>
	IV - suspensão  temporária de participar em licitação e impedimento de contratar com a admi- nistração pública por prazo não superior a 2 (dois) anos; e<br>
	V - declaração  de inidoneidade para licitar ou contratar  com a administração pública, até que seja promovida a reabilitação perante a própria autoridade que aplicou a penalidade.<br>
	<p>&nbsp;</p>
	§ 1º  .  As sanções  previstas  nos incisos I, III e IV poderão  ser  aplicadas  juntamente  com a do inciso II, assegurado  o direito de defesa do interessado, no respectivo processo, no prazo de 10 (dez) dias.<br>
	§ 2º . A reabilitação referida no inciso V será autorizada somente quando o interessado efetivar o ressarcimento ao órgão ou entidade dos prejuízos resultantes e após decorrido o prazo da sanção  aplicada com base  no inciso IV.<br>
	§ 3º . A aplicação da sanção prevista no inciso V é de competência  exclusiva da autoridade má- xima do órgão ou entidade  pública, facultada a defesa do interessado, no respectivo processo, no prazo de 10 (dez) dias da abertura de vista.</p>
	<p>Art. 17. Os órgãos  e entidades públicas respondem diretamente pelos danos causados em decorrência da divulgação não autorizada ou utilização indevida de informações sigilosas ou informações pessoais, cabendo a apuração de responsabilidade  funcional nos casos  de dolo ou culpa, assegurado o respectivo direito de regresso.
	Parágrafo único. O disposto  neste  artigo aplica-se  à pessoa  física ou entidade privada que, em virtude de vínculo de qualquer natureza com órgãos ou entidades, tenha acesso a informação sigilosa ou pessoal e a submeta a tratamento indevido.</p>
	<p>&nbsp;</p>
	<p>Art.  18. A   Secretaria   Municipal de Planejamento, Fazenda  e Tecnologia  da Informação, desenvolverá atividades para:<br>
	<p>&nbsp;</p>
	I - promoção de campanha de abrangência  municipal de fomento à cultura da transparência na administração pública e conscientização do direito fundamental de acesso à informação;<br>
	II   - treinamento  dos agentes  públicos e, no que couber,  a capacitação  das entidades privadas sem fins lucrativos, no que se refere ao desenvolvimento de práticas relacionadas à transparência na administração pública;<br>
	III - monitoramento dos prazos e procedimentos de acesso à informação;<br>
	IV - definição do formulário padrão,  disponibilizado em meio físico e eletrônico, que estará à disposição na Internet e no Serviço de Informação ao Cidadão - SIC.</p>
	<p>&nbsp;</p>
	<p>Art.  19. Na  aplicação  deste decreto  serão observadas  as questões  sobre classificação de informações secretas,   sigilosas  e reservadas,  o acesso a informações pessoais,  a responsabilidade sobre o acesso e divulgação de informações e as disposições do Decreto Federal n. 7.724, de 16 de maio de 2012.</p>
	<p>&nbsp;</p>
	<p>Art. 20. Este decreto  entra  em vigor 60 (sessenta) dias após sua publicação.</p>
